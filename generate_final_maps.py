# Proponowane SOI dla pełnego obciążenia (WOT) - ochrona PCP
proposed_wot_soi = {
    600: 0.0, 750: 0.0, 1000: 0.0, 1250: 3.5, 1500: 4.5, 1750: 5.5, 
    2000: 6.5, 2250: 6.5, 2500: 7.0, 2750: 7.5, 3000: 9.0, 3250: 10.0, 
    3500: 11.0, 3750: 12.0, 4000: 13.0, 4500: 15.0
}

new_soi = []
for r_idx, rpm in enumerate(rpms):
    row = []
    wot_target = proposed_wot_soi[rpm]
    
    for c_idx, iq in enumerate(iqs):
        ori_val = ori_soi[r_idx][c_idx]
        
        # Blending logic
        # For IQ < 14.6, keep mostly original (part throttle)
        # For IQ >= 14.6, start blending towards proposed WOT value
        if iq <= 12.9:
            # Minimalne przyspieszenie SOI na bardzo niskich obrotach by pomóc turbo wstawać
            if 1250 <= rpm <= 1750:
                 new_val = ori_val + 0.5
            else:
                 new_val = ori_val
        else:
            # WOT blending
            # Interpolate blend factor based on IQ
            # 14.6 -> 20% proposed, 80% ori
            # 50.0 -> 100% proposed
            blend_factor = (iq - 12.9) / (50.0 - 12.9)
            blend_factor = max(0.0, min(1.0, blend_factor))
            
            # For high RPM (>= 2250) we MUST protect PCP heavily, override ori totally at top
            if rpm >= 2250 and iq >= 31.4:
                blend_factor = 1.0
                
            new_val = ori_val * (1 - blend_factor) + wot_target * blend_factor
            
        row.append(round(new_val, 2))
    new_soi.append(row)

with open('NEW_SOI_MAP.csv', 'w', encoding='utf-8') as f:
    f.write("RPM \\ IQ;" + ";".join([str(x) for x in iqs]) + "\n")
    for r_idx, rpm in enumerate(rpms):
        row_strs = [str(x) for x in new_soi[r_idx]]
        f.write(f"{rpm};" + ";".join(row_strs) + "\n")

print("Zapisano NEW_SOI_MAP.csv")

