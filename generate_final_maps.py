    val_at_3000 = ori_rail[r_idx][11]
    val_at_4000 = ori_rail[r_idx][15]
    
    # Simple linear extrapolation for new points, clamped at 1700
    for new_iq in [3700, 4500, 5500, 6800]:
        if new_iq <= 4000:
            # Interpolate
            t = (new_iq - 3000) / (4000 - 3000)
            new_val = val_at_3000 + t * (val_at_4000 - val_at_3000)
        else:
            # Extrapolate
            t = (new_iq - 4000) / (4000 - 3000)
            new_val = val_at_4000 + t * (val_at_4000 - val_at_3000)
            
        new_val = min(1700, max(0, new_val))
        row.append(int(new_val))
        
    new_rail.append(row)

