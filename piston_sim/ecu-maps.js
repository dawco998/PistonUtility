// ── Interpolation helpers ──
function ecuInterp1d(xArr, yArr, x) {
  if (x <= xArr[0]) return yArr[0];
  if (x >= xArr[xArr.length-1]) return yArr[yArr.length-1];
  for (let i = 0; i < xArr.length-1; i++) {
    if (xArr[i] <= x && x <= xArr[i+1]) {
      const t = (x - xArr[i]) / (xArr[i+1] - xArr[i]);
      return yArr[i] + t * (yArr[i+1] - yArr[i]);
    }
  }
  return yArr[yArr.length-1];
}

function ecuInterp2d(map, rpm, xVal) {
  const keys = map.rpmKeys;
  if (rpm <= keys[0]) return ecuInterp1d(map.iqAxis || map.nmAxis, map.data[keys[0]], xVal);
  if (rpm >= keys[keys.length-1]) return ecuInterp1d(map.iqAxis || map.nmAxis, map.data[keys[keys.length-1]], xVal);
  for (let i = 0; i < keys.length-1; i++) {
    if (keys[i] <= rpm && rpm <= keys[i+1]) {
      const axis = map.iqAxis || map.nmAxis;
      const v1 = ecuInterp1d(axis, map.data[keys[i]], xVal);
      const v2 = ecuInterp1d(axis, map.data[keys[i+1]], xVal);
      const t = (rpm - keys[i]) / (keys[i+1] - keys[i]);
      return v1 + t * (v2 - v1);
    }
  }
}

// ── ECU Lookup Functions ──
ECU.getNmLimit = function(rpm) {
  return ecuInterp1d(ECU.nmLimiter.rpm, ECU.nmLimiter.nm, rpm);
};

ECU.getIq = function(rpm, nm) {
  return ecuInterp2d({...ECU.nmToIq, iqAxis: undefined, nmAxis: ECU.nmToIq.nmAxis}, rpm, nm * 10);
};

ECU.getRail = function(rpm, iq_mm3) {
  return ecuInterp2d(ECU.rail, rpm, Math.min(iq_mm3 * 100, 4000));
};

ECU.getDuration = function(rail_bar, iq_mm3) {
  // Rail axis in Duration map = bar * 10
  const railVal = rail_bar * 10;
  const iqVal = iq_mm3 * 100;
  const keys = ECU.duration.railKeys;
  if (railVal <= keys[0]) return ecuInterp1d(ECU.duration.iqAxis, ECU.duration.data[keys[0]], iqVal);
  if (railVal >= keys[keys.length-1]) return ecuInterp1d(ECU.duration.iqAxis, ECU.duration.data[keys[keys.length-1]], iqVal);
  for (let i = 0; i < keys.length-1; i++) {
    if (keys[i] <= railVal && railVal <= keys[i+1]) {
      const d1 = ecuInterp1d(ECU.duration.iqAxis, ECU.duration.data[keys[i]], iqVal);
      const d2 = ecuInterp1d(ECU.duration.iqAxis, ECU.duration.data[keys[i+1]], iqVal);
      const t = (railVal - keys[i]) / (keys[i+1] - keys[i]);
      return d1 + t * (d2 - d1);
    }
  }
};

ECU.getSoi = function(rpm, iq_mm3) {
  const iq_mg = iq_mm3 * 0.835;
  return ecuInterp2d(ECU.soi, rpm, iq_mg);
};

ECU.getInjDelay = function(rail_bar) {
  return Math.max(180, Math.min(400 - 0.12 * rail_bar, 400));
};

