// Returns demanded Nm from pedal map (before NM limiter clamp)
ECU.getPedalNm = function(rpm, pedalPct) {
  const keys = ECU.pedalMap.rpmKeys;
  const axis = ECU.pedalMap.pedalAxis;
  const data = ECU.pedalMap.data;
  let rawVal;
  if (rpm <= keys[0]) { rawVal = ecuInterp1d(axis, data[keys[0]], pedalPct); }
  else if (rpm >= keys[keys.length-1]) { rawVal = ecuInterp1d(axis, data[keys[keys.length-1]], pedalPct); }
  else {
    for (let i = 0; i < keys.length-1; i++) {
      if (keys[i] <= rpm && rpm <= keys[i+1]) {
        const v1 = ecuInterp1d(axis, data[keys[i]], pedalPct);
        const v2 = ecuInterp1d(axis, data[keys[i+1]], pedalPct);
        const t = (rpm - keys[i]) / (keys[i+1] - keys[i]);
        rawVal = v1 + t * (v2 - v1);
        break;
      }
    }
  }
  return rawVal || 0;
};

ECU.getPedalIq = function(rpm, pedalPct) {
  // Pedal map → demanded Nm → clamp by NM limiter → NM_TO_IQ → IQ mm3
  const demandedNm = ECU.getPedalNm(rpm, pedalPct);
  const nmMax = ECU.getNmLimit(rpm);
  const clampedNm = Math.min(demandedNm, nmMax);
  return ECU.getIq(rpm, clampedNm);
};

// ── Full WOT lookup: RPM → all params ──
ECU.lookupWOT = function(rpm) {
  const nmMax = ECU.getNmLimit(rpm);
  const iq = ECU.getIq(rpm, nmMax);
  const rail = ECU.getRail(rpm, iq);
  const dur_us = ECU.getDuration(rail, iq);
  const injDelay = ECU.getInjDelay(rail);
  const soi = ECU.getSoi(rpm, iq);
  const totalDur_us = dur_us + injDelay;
  const totalDur_deg = totalDur_us * rpm * 6 / 1e6;
  const eoi = -soi + totalDur_deg;
  
  const logRpms = Object.keys(ECU.logWot).map(Number).sort((a,b)=>a-b);
  const boostVals = logRpms.map(r => (ECU.logWot[r].map_kpa / 100) - 1.01325);
  const boost = ecuInterp1d(logRpms, boostVals, rpm);
  
  return { nmMax, iq, rail, dur_us, injDelay, soi, totalDur_us, totalDur_deg, eoi, boost, pedal: 100 };
};

// ── Partial Load lookup: RPM + pedal% → all params ──
ECU.lookupPartial = function(rpm, pedalPct) {
  if (pedalPct >= 99) return ECU.lookupWOT(rpm);
  
  const iq = ECU.getPedalIq(rpm, pedalPct);
  const nmMax = ECU.getNmLimit(rpm);
  // Clamp IQ to NM limiter equivalent
  const iqMax = ECU.getIq(rpm, nmMax);
  const iqClamped = Math.min(iq, iqMax);
  
  const rail = ECU.getRail(rpm, iqClamped);
  const dur_us = iqClamped > 0.5 ? ECU.getDuration(rail, iqClamped) : 0;
  const injDelay = ECU.getInjDelay(rail);
  const soi = ECU.getSoi(rpm, iqClamped);
  const totalDur_us = dur_us + injDelay;
  const totalDur_deg = totalDur_us * rpm * 6 / 1e6;
  const eoi = -soi + totalDur_deg;
  
  // Boost scales roughly with load (simplified)
  const logRpms = Object.keys(ECU.logWot).map(Number).sort((a,b)=>a-b);
  const boostVals = logRpms.map(r => (ECU.logWot[r].map_kpa / 100) - 1.01325);
  const boostMax = ecuInterp1d(logRpms, boostVals, rpm);
  const loadFraction = iqMax > 0 ? iqClamped / iqMax : 0;
  const boost = Math.max(0, boostMax * Math.pow(loadFraction, 1.5));
  
  return { nmMax, iq: iqClamped, rail, dur_us, injDelay, soi, totalDur_us, totalDur_deg, eoi, boost, pedal: pedalPct };
};
